const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const wishlistValidation = require('../../validations/Wishlist.validation');
const wishlistController = require('../../controllers/wishlist.controller');

const router = express.Router();

router
  .route('/')
  .post(auth(), validate(wishlistValidation.createWishlist), wishlistController.createWishlist)
  .get(auth(), validate(wishlistValidation.getWishlists), wishlistController.getWishlists);

router
  .route('/:listId')
  .get(auth(), validate(wishlistValidation.getWishlist), wishlistController.getWishlist)
  .patch(auth(), validate(wishlistValidation.updateWishlist), wishlistController.updateWishlist)
  .delete(auth(), validate(wishlistValidation.deleteWishlist), wishlistController.deleteWishlist);

module.exports = router;

